//1. Write a 'C' program using while loo. to print "hello" message 5 times.
#include <stdio.h>

int main() {
    int i = 0; // initialize a counter variable

    while (i < 5) { // loop until i is less than 5
        printf("hello\n"); // print the "hello" message
        i++; // increment the counter variable
    }

    return 0;
}